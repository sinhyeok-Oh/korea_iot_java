// === 컬렉션 프레임워크 === //

// cf) 프레임워크 (framework)
// : frame(틀) + work(일하다)
// - 제공받은 일정한 요소와 틀, 규약을 가지고 우언가를 만드는 일
// 1. 컬렉션 프리임워크(Collection Framework)
// : 데이터를 정하고 처리하는 자료구조의 표준화 된 설계
// - 다양한 자료구조를 제공, 데이터의 효울적인 관리를 지원

// 2. 컬력션 프레임워크의 주요 '인터페이스' 와 특성
// 1) Collection
//      : 자바 CF의 최상위 인터페이스 (List, Set 등이 하위 인터페이스)
//      - 데이터의 집합을다룸

// 2) List
//      : 데이터의 순서 유지 o (index 번화)
//      - 중복 가능
//      - 주요 구현체: ArrayList, LinkedList, Vector 등

// 3) Set
//      : 데이터의 순서 유지 x, 중복 불가능 (cf, 특정 구현체는 정렬 | 순서 제공 가능)
//      - 주요 구현체: HashSet, TreeSet, LinkedHashSet

// 4) Map
//      : 키(key) - 값(value) 쌍으로 이루어진 데이터를 다룸
//      - 키(중복 혀용 x), 값 (중복 허용o)
//      - 주요 구현체: HashSet, TreeSet, LinkedHashMap

// cf) Queue(큐): FIFO (First In First Out)
//      Stack(스택): LIFO (Last In First Out)

